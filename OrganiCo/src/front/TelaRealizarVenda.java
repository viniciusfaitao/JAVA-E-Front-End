    public void preencherTabela(){
        
        DefaultTableModel model = (DefaultTableModel) tabelaPesquisa1.getModel();
        tabelaPesquisa1.setModel(model);
        Object rowData[] = new Object[5];
            for(int i=0; i < manFun.produtos.size(); i++){
                rowData[0] = manFun.produtos.get(i).tipo;
                rowData[1] = manFun.produtos.get(i).nome;
                rowData[2] = manFun.produtos.get(i).dataColheita;
                rowData[3] = manFun.produtos.get(i).qEstoque;
                rowData[4] = manFun.produtos.get(i).valor;
    
                model.addRow(rowData);
            }
        
        tabelaPesquisa1.getTableHeader().setReorderingAllowed(false);
        tabelaPesquisa1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
